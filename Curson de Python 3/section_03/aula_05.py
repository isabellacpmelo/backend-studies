# Tipo de dado bool (boleano)
# Ao questionar algo em um programa só existem duas respostas possíveis:
# Sim (True) ou Nao (False).
# Existem duas operações para "questionar" algo em Python:
# Dentre eles o == (igual) e o != (diferente), que são operadores lógicos e questionam se algo e o outro algo sao iguais ou diferentes

print(10 == 10, 10 == 5, 10 != 5, sep='\n')