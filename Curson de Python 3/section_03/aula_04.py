# Tipos int e float
# int -> Números inteiros
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado positivo.
print(11, type(11), sep=' -> ')
print(-11, type(-11), sep=' -> ')
print(0, type(0), sep=' -> ')

# float -> Número com ponto flutuante
# O tipo float representa qualquer um número positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, type(1.1), sep=' -> ')

# A função type mostra o tipo que o Python inferiu ao valor
print(type('Isabella'))