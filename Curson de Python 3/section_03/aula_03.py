"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string (TEXTO)
Strings são textos que estão dentro de aspas (simples ou duplas)
"""

# Aspas simples
print('Isabella Melo', 'Usando aspas simples', sep=' - ')

# Aspas duplas
print("Isabella Melo", "Usando aspas duplas", sep=" - ")

# Escape
print("Isso é um escape: Aspas duplas (\")")
print("Isso é outro escape: Aspas simples: (\')")

# r -> raw string (nao faz o escape)
print(r"Mostrando o caractere especial de escape: (\')")